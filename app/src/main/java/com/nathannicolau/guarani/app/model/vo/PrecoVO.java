package com.nathannicolau.guarani.app.model.vo;


public class PrecoVO {

    private String idPreco;
    private String prpCodigoVO;
    private String prpPrecosVO;

    public String getIdPreco() {
        return idPreco;
    }

    public void setIdPreco(String idPreco) {
        this.idPreco = idPreco;
    }

    public String getPrpCodigoVO() {
        return prpCodigoVO;
    }

    public void setPrpCodigoVO(String prpCodigoVO) {
        this.prpCodigoVO = prpCodigoVO;
    }

    public String getPrpPrecosVO() {
        return prpPrecosVO;
    }

    public void setPrpPrecosVO(String prpPrecosVO) {
        this.prpPrecosVO = prpPrecosVO;
    }
}
